const SIZE_CLASSES = {
  xs: 'icon-xs',
  sm: 'icon-sm',
  md: 'icon-md',
  lg: 'icon-lg',
  xl: 'icon-xl',
};

/**
 * Icon component - Renders SVG icons
 * @param {string} name - Icon name from ICONS object
 * @param {string} size - Size: xs (12px), sm (16px), md (20px), lg (24px), xl (32px)
 * @param {string} className - Additional CSS classes
 * @param {object} props - Additional props passed to svg element
 */
export function Icon({ name, size = 'md', className = '', ...props }) {
  const iconSvg = ICONS[name];

  if (!iconSvg) {
    console.warn(`Icon "${name}" not found. Available icons:`, Object.keys(ICONS).join(', '));
    return null;
  }

  const sizeClass = SIZE_CLASSES[size] || SIZE_CLASSES.md;

  return (
    <span className={`icon ${sizeClass} ${className}`} {...props}>
      {iconSvg}
    </span>
  );
}

